CREATE TABLE PRODUCT(
	P_CODE INTEGER AUTO_INCREMENT PRIMARY KEY,
    P_DESCRIPT VARCHAR(35) NOT NULL,
    P_INDATE DATE NOT NULL,
    P_QOH INTEGER NOT NULL,
    P_MIN INTEGER NOT NULL,
    P_PRICE DECIMAL(8,2) NOT NULL,
    P_DISCOUNT DECIMAL(5,2) DEFAULT 0.00 NOT NULL CHECK(P_DISCOUNT BETWEEN 0 AND 1)
);
CREATE TABLE INVENTORY_LOG(
	P_CODE INTEGER NOT NULL REFERENCES PRODUCT(P_CODE),
    LOG_NUMBER INTEGER NOT NULL,
    LOG_DESCRIPT ENUM('RESTOCK', 'RETURN', 'DEFECTIVE', 'ADJUST IN', 'ADJUST OUT') NOT NULL,
    LOG_INDATE DATE NOT NULL,
    LOG_UNITS INTEGER NOT NULL CHECK(LOG_UNITS>0),
    PRIMARY KEY(P_CODE, LOG_NUMBER)
);
CREATE TABLE CUSTOMER(
	CUS_CODE INTEGER AUTO_INCREMENT PRIMARY KEY,
    CUS_NAME VARCHAR(10) NOT NULL,
    CUS_PHONE CHAR(10) NOT NULL,
    CUS_BALANCE DECIMAL(9,2) DEFAULT 0.00
);
CREATE TABLE INVOICE(
	INV_NUMBER INTEGER AUTO_INCREMENT PRIMARY KEY,
    CUS_CODE INTEGER NOT NULL REFERENCES CUSTOMER(CUS_CODE),
    INV_DATE DATETIME DEFAULT NOW() NOT NULL,
    INV_PAYMENT ENUM('CASH', 'CARD', 'BANK_TRANSFER', 'E_WALLET'),
    INV_PAID BOOLEAN NOT NULL DEFAULT 0,
    INV_TRANSCODE VARCHAR(50),
    EMP_CODE INTEGER NOT NULL REFERENCES EMPLOYEE(EMP_CODE)
);
CREATE TABLE LINE(
	INV_NUMBER INTEGER NOT NULL REFERENCES INVOICE(INV_NUMBER) ON DELETE CASCADE,
    LINE_NUMBER DECIMAL(2,0) NOT NULL,
    P_CODE VARCHAR(10) NOT NULL REFERENCES PRODUCT(P_CODE),
    LINE_UNITS INTEGER NOT NULL,
    LINE_PRICE DECIMAL(9,2) NOT NULL,
    PRIMARY KEY(INV_NUMBER, LINE_NUMBER),
    CONSTRAINT UNIQUE(INV_NUMBER, P_CODE)
);
CREATE TABLE EMPLOYEE(
	EMP_CODE INTEGER AUTO_INCREMENT PRIMARY KEY,
    EMP_LNAME VARCHAR(10) NOT NULL,
    EMP_FNAME VARCHAR(10) NOT NULL,
    EMP_JOB VARCHAR(50) NOT NULL
);

