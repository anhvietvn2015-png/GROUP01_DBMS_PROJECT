CREATE VIEW INVOICE_SUMMARY AS(
SELECT
	INV_NUMBER,
    INV_DATE,
    INV_PAID,
    CUS_CODE,
    CUS_NAME,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_AMOUNT
FROM INVOICE
JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
JOIN CUSTOMER ON INVOICE.CUS_CODE = CUSTOMER.CUS_CODE
GROUP BY INV_NUMBER, INV_DATE, INV_PAID, CUS_CODE, CUS_NAME
);
CREATE VIEW SALES_BY_DAY AS(
SELECT
	DATE(INV_DATE) AS 'DATE',
    COUNT(DISTINCT INV_NUMBER) AS TOTAL_INVOICES,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_SALES
FROM INVOICE JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
GROUP BY DATE(INV_DATE)
);
CREATE VIEW SALES_BY_PRODUCT AS(
SELECT 
	P_CODE,
    P_DESCRIPT,
    P_QOH,
    P_MIN,
    SUM(LINE_UNITS) AS UNITS_SOLD,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_SALES
FROM PRODUCT JOIN LINE ON PRODUCT.P_CODE = LINE.P_CODE
GROUP BY P_CODE, P_DESCRIPT
);
CREATE VIEW SALES_BY_CUSTOMER AS(
SELECT
	CUS_CODE,
    CUS_NAME,
    CUS_BALANCE,
    COUNT(DISTINCT INV_NUMBER) AS TOTAL_INVOICES,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_SALES
FROM CUSTOMER 
JOIN INVOICE ON CUSTOMER.CUS_CODE = INVOICE.CUS_CODE
JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
GROUP BY CUS_CODE, CUS_NAME, CUS_BALANCE
);
CREATE VIEW SALES_BY_EMPLOYEE AS(
SELECT
	EMP_CODE,
    EMP_NAME,
    COUNT(DISTINCT INV_NUMBER) AS TOTAL_INVOICES,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_SALES
FROM EMPLOYEE 
JOIN INVOICE ON EMPLOYEE.EMP_CODE = INVOICE.EMP_CODE
JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
GROUP BY EMP_CODE, EMP_NAME
);
CREATE VIEW PRODUCT_LOW_STOCK AS(
SELECT 
	P_CODE,
    P_DESCRIPT,
    (P_MIN - P_QOH) AS SHORTAGE
FROM PRODUCT
WHERE P_QOH < P_MIN
);
CREATE VIEW INVOICE_UNPAID AS(
SELECT 
	INV_NUMBER,
    INV_DATE,
    CUS_CODE,
    CUS_NAME,
    SUM(LINE_UNITS*LINE_PRICE) AS TOTAL_AMOUNT
FROM INVOICE
JOIN CUSTOMER ON INVOICE.CUS_CODE = CUSTOMER.CUS_CODE
JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
WHERE INV_PAID = 0
GROUP BY INV_NUMBER, INV_DATE, CUS_CODE, CUS_NAME
);

